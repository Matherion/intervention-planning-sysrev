### Activity
###   Performance objectives are formulated according to the definition
###
### What to look for in the report
###   All performance objectives are behaviors: observable activities
###   that individuals need to complete to perform the desired behavior

################################################################################
### Activity
###   Determinants are formulated according to the definition
###
### What to look for in the report
###   All determinants are modifiable psychological variables

################################################################################
### Activity
###   Change objectives are formulated according to the definition
###
### What to look for in the report
###   Each change objective concerns a specification of an aspect of a
###   determinant of a performance objective. Change objectives are
###   formulated as an action that, when exhibited by the target
###   population, evidences the desired value of the sub-determinant
###   (for example, using the action verbs from the IM book). In other
###   words, change objectives are 'SMART' goals that, when achieved,
###   guarantee a relevant change in the corresponding determinant.

